// Usage: Scale the picture to fit the maxmium 'width' or 'height'.
// Example: original picture: 1000*2000, width: 500, height: null  => Output: (500*1000)
export const autoSize = (source, width, height) => {
  const image = Image.resolveAssetSource(source);
  const ratio = image.width / image.height;

  if (width && !height) {
    return {
      width: width,
      height: width / ratio,
    };
  }
  if (height && !width) {
    return {
      height: height,
      width: height * ratio,
    };
  }
  if (!height && !width) {
    return {
      width: 60,
      height: 60,
    };
  }
};

export const getRandomGameItem = () => {
  var keys = Object.keys(IconManager.gameItem);
  return IconManager.gameItem[keys[(keys.length * Math.random()) << 0]];
};
